package com.example.watersystem.home.analysis.type;

public class HomeAnalysisType {

    private Integer homeAnalysisImg;
    private String homeAnalysisTitle;
    private String homeAnalysisTds;
    private String homeAnalysisPh;
    private String homeAnalysisTemp;
    private String homeAnalysisTu;
    private boolean homeAnalysisCheck;

    public int getHomeAnalysisImg() {
        return homeAnalysisImg;
    }

    public void setHomeAnalysisImg(Integer homeAnalysisImg) {
        this.homeAnalysisImg = homeAnalysisImg;
    }

    public String getHomeAnalysisTitle() {
        return homeAnalysisTitle;
    }

    public void setHomeAnalysisTitle(String homeAnalysisTitle) {
        this.homeAnalysisTitle = homeAnalysisTitle;
    }

    public String getHomeAnalysisTds() {
        return homeAnalysisTds;
    }

    public void setHomeAnalysisTds(String homeAnalysisTds) {
        this.homeAnalysisTds = homeAnalysisTds;
    }

    public String getHomeAnalysisPh() {
        return homeAnalysisPh;
    }

    public void setHomeAnalysisPh(String homeAnalysisPh) {
        this.homeAnalysisPh = homeAnalysisPh;
    }

    public String getHomeAnalysisTemp() {
        return homeAnalysisTemp;
    }

    public void setHomeAnalysisTemp(String homeAnalysisTemp) {
        this.homeAnalysisTemp = homeAnalysisTemp;
    }

    public String getHomeAnalysisTu() {
        return homeAnalysisTu;
    }

    public void setHomeAnalysisTu(String homeAnalysisTu) {
        this.homeAnalysisTu = homeAnalysisTu;
    }

    public boolean isHomeAnalysisCheck() {
        return homeAnalysisCheck;
    }

    public void setHomeAnalysisCheck(boolean homeAnalysisCheck) {
        this.homeAnalysisCheck = homeAnalysisCheck;
    }
}
